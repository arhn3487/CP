void solve()
{
    ll n;cin>>n;
    string s="";

    if(!n)
    {
        cout<<0<<'\n';
        return;
    }

    while(n)
    {
        int t=n%(-2);
        char ch=(t==-1 ||t==1) ? '1' : '0';
        s.push_back(ch);
        //cout<<n%(-2)<<'\n';
        n/=-2;
        if(t<0) n++;
    }
    reverse(all(s));
    cout<<s<<'\n';
}

